#include <stdio.h>

int main() {
    // Declare an array to store 5 numbers
    int numbers[] = {1, 2, 3, 4, 5, 6};

    // Loop through the array and print each number
    for (int i = 0; i < 6; i++) {
        printf("Numbers:%d\n", numbers[i]);
    }
    printf("\n");
    printf("Done\n");

    return 0;
}
